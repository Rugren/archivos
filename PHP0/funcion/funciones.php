<?php

    // funciones tipo calculadora
    function suma($s1, $s2){
        return $s1 + $s2;
    }

    function resta($s1, $s2){
        return $s1 - $s2;
    }

    function multiplicacion($s1, $s2){
        return $s1 * $s2;
    }

    function division($s1, $s2){
        return $s1 / $s2;
    }

    function raizCuadrada($sn){
        return sqrt ($sn);
    }

?>

